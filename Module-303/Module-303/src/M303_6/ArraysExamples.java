public class ArraysExamples {

    public static void main(String[] args) {

        int[] numbers = new int[10];
        for(int i = 0; i < numbers.length; i++){
            numbers[i] = i+1 ;
            System.out.println("Number " +numbers[i]+" is at position "+i);
        }

        //Dynamic Array






    }
}
